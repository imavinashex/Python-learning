#what is Arithmetic operaters in python
# Arithmetic operators are used to perform mathematical operations between numeric values.
# Here are the common Arithmetic operators in Python:
# Addition (+): Adds two numbers together.
# Subtraction (-): Subtracts one number from another.  
# Multiplication (*): Multiplies two numbers.
# Division (/): Divides one number by another and returns a float.
# Floor Division (//): Divides one number by another and returns the largest integer less than or equal to the result.
# Modulus (%): Returns the remainder of the division of one number by another.
# Exponentiation (**): Raises one number to the power of another.
#thats in Arithmetic operaters in python
#here is the example code for Arithmetic operaters in python

a = 10
b = 3
# Addition
print("Addition:", a + b)  # Output: 13
# Subtraction
print("Subtraction:", a - b)  # Output: 7
# Multiplication
print("Multiplication:", a * b)  # Output: 30
# Division
print("Division:", a / b)  # Output: 3.3333333333333335
# Floor Division
print("Floor Division:", a // b)  # Output: 3
# Modulus
print("Modulus:", a % b)  # Output: 1
# Exponentiation 
print("Exponetition:", a ** b)  # Output: 1000


